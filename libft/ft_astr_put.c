#include "libft.h"

void	ft_astr_put(char **astr)
{
	int		i;

	if (astr && *astr)
	{
		i = -1;
		while (astr[++i])
		{
			ft_putstr(astr[i]);
			ft_putchar('\n');
		}
	}
}
